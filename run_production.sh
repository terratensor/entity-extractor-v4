#!/bin/bash

# Продакшн-запуск конвейера v4

echo "========================================="
echo "🚀 ЗАПУСК КОНВЕЙЕРА V4 (PRODUCTION)"
echo "========================================="

# Проверка наличия конфига
if [ ! -f "config_v4.yaml" ]; then
    echo "❌ Ошибка: config_v4.yaml не найден!"
    exit 1
fi

# Активируем виртуальное окружение
source venv/bin/activate

# Создаём директорию для результатов
mkdir -p results_v4

# Запускаем
python -m app.v4.main --config config_v4.yaml --resume

# Сохраняем статус выхода
EXIT_CODE=$?

if [ $EXIT_CODE -eq 0 ]; then
    echo "========================================="
    echo "✅ ОБРАБОТКА УСПЕШНО ЗАВЕРШЕНА"
    echo "========================================="
else
    echo "========================================="
    echo "❌ ОБРАБОТКА ПРЕРВАНА С КОДОМ $EXIT_CODE"
    echo "========================================="
fi

exit $EXIT_CODE